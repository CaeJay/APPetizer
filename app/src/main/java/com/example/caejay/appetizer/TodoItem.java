public class TodoItem {
    private String tname; //item name
    private String todo;
    private int id; //item name



    public String getTName() {
        return tname;
    }

    public void setTName(String name) {
        this.tname = name;
    }

    public String getTodo() {
        return todo;
    }

    public void setTodo(String todo) {
        this.todo = todo;
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


}
